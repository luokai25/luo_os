/// Software framebuffer — renders to a pixel buffer
/// In production this maps to VGA/VESA memory
/// Here we use a Vec<u8> and output via terminal escape codes

use crate::types::{Color, Rect, Point};

pub const SCREEN_W: u32 = 1024;
pub const SCREEN_H: u32 = 768;

pub struct Framebuffer {
    pub width:  u32,
    pub height: u32,
    pub buf:    Vec<u8>,   // RGBA, 4 bytes per pixel
}

impl Framebuffer {
    pub fn new() -> Self {
        Self {
            width:  SCREEN_W,
            height: SCREEN_H,
            buf:    vec![0u8; (SCREEN_W * SCREEN_H * 4) as usize],
        }
    }

    #[inline]
    pub fn set_pixel(&mut self, x: i32, y: i32, c: Color) {
        if x < 0 || y < 0 || x >= self.width as i32 || y >= self.height as i32 {
            return;
        }
        let idx = ((y as u32 * self.width + x as u32) * 4) as usize;
        self.buf[idx]     = c.r;
        self.buf[idx + 1] = c.g;
        self.buf[idx + 2] = c.b;
        self.buf[idx + 3] = 255;
    }

    pub fn fill_rect(&mut self, r: Rect, c: Color) {
        for dy in 0..r.h as i32 {
            for dx in 0..r.w as i32 {
                self.set_pixel(r.x + dx, r.y + dy, c);
            }
        }
    }

    pub fn draw_rect_outline(&mut self, r: Rect, c: Color, thickness: u32) {
        for t in 0..thickness as i32 {
            // top
            for x in r.x..r.x + r.w as i32 {
                self.set_pixel(x, r.y + t, c);
            }
            // bottom
            for x in r.x..r.x + r.w as i32 {
                self.set_pixel(x, r.y + r.h as i32 - 1 - t, c);
            }
            // left
            for y in r.y..r.y + r.h as i32 {
                self.set_pixel(r.x + t, y, c);
            }
            // right
            for y in r.y..r.y + r.h as i32 {
                self.set_pixel(r.x + r.w as i32 - 1 - t, y, c);
            }
        }
    }

    pub fn draw_line(&mut self, x0: i32, y0: i32, x1: i32, y1: i32, c: Color) {
        let dx = (x1 - x0).abs();
        let dy = (y1 - y0).abs();
        let sx = if x0 < x1 { 1 } else { -1 };
        let sy = if y0 < y1 { 1 } else { -1 };
        let mut err = dx - dy;
        let mut x = x0; let mut y = y0;
        loop {
            self.set_pixel(x, y, c);
            if x == x1 && y == y1 { break; }
            let e2 = 2 * err;
            if e2 > -dy { err -= dy; x += sx; }
            if e2 < dx  { err += dx; y += sy; }
        }
    }

    /// Draw a filled circle
    pub fn fill_circle(&mut self, cx: i32, cy: i32, r: i32, c: Color) {
        for y in -r..=r {
            for x in -r..=r {
                if x*x + y*y <= r*r {
                    self.set_pixel(cx + x, cy + y, c);
                }
            }
        }
    }

    /// Simple 5x7 bitmap font — draws a single character
    pub fn draw_char(&mut self, ch: char, x: i32, y: i32, fg: Color) {
        let bm = char_bitmap(ch);
        for (row, byte) in bm.iter().enumerate() {
            for col in 0..5u32 {
                if byte & (1 << (4 - col)) != 0 {
                    self.set_pixel(x + col as i32, y + row as i32, fg);
                }
            }
        }
    }

    pub fn draw_text(&mut self, text: &str, mut x: i32, y: i32, fg: Color) {
        for ch in text.chars() {
            self.draw_char(ch, x, y, fg);
            x += 6; // 5px char + 1px gap
        }
    }

    pub fn draw_text_centered(&mut self, text: &str, rect: Rect, fg: Color) {
        let text_w = (text.len() as i32) * 6;
        let text_h = 7;
        let x = rect.x + (rect.w as i32 - text_w) / 2;
        let y = rect.y + (rect.h as i32 - text_h) / 2;
        self.draw_text(text, x, y, fg);
    }

    /// Clear entire screen
    pub fn clear(&mut self, c: Color) {
        for i in (0..self.buf.len()).step_by(4) {
            self.buf[i]     = c.r;
            self.buf[i + 1] = c.g;
            self.buf[i + 2] = c.b;
            self.buf[i + 3] = 255;
        }
    }

    /// Get pixel color at position
    pub fn get_pixel(&self, x: i32, y: i32) -> Color {
        if x < 0 || y < 0 || x >= self.width as i32 || y >= self.height as i32 {
            return Color::BLACK;
        }
        let idx = ((y as u32 * self.width + x as u32) * 4) as usize;
        Color::rgb(self.buf[idx], self.buf[idx+1], self.buf[idx+2])
    }
}

